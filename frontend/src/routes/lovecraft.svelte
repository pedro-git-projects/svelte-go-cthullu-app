<h2>Quem foi H.P Lovecraft</h2>
<hr>

<p>
	Howard Phillips Lovecraft (Providence, Rhode Island, 20 de agosto de 1890 — Providence, Rhode Island, 15 de março de 1937), mais conhecido como H. P. Lovecraft, foi um escritor estadunidense que revolucionou o gênero de terror, ficção estranha, científica, fantasia e horror, atribuindo-lhe elementos fantásticos típicos dos gêneros de fantasia e ficção científica, sendo mais conhecido por sua criação dos Mitos de Cthulhu. 
</p>


<p>
	Lovecraft originou o ciclo de histórias que, posteriormente, foram agrupadas nos Mitos de Cthulhu e o grimório fictício conhecido como Necronomicon — atribuído em suas histórias a um estranho árabe chamado Abdul Al Hazred — através do qual os seres humanos em suas histórias entravam em contato com o panteão de entidades criadas pelo autor. Lovecraft era assumidamente conservador e anglófilo, o que pode ser observado em seu poema An American To Mother England, publicado em janeiro de 1916. Seu estilo literário emprega arcaísmos, vocabulário e ortografia marcadamente britânicos — fato que contribui para aumentar a atmosfera de suas histórias, como no conto O Caso de Charles Dexter Ward, que contêm referências a personagens que viveram antes da independência das Treze Colônias, bem como a comércios existentes entre os séculos XVII e XVIII.
</p>

<h2>Obra</h2>
<hr>

<p>
	Muitos dos trabalhos de Lovecraft foram diretamente inspirados por seus constantes pesadelos, o que contribuiu para a criação de uma obra marcada pelo subconsciente e pelo simbolismo. Suas maiores influências foram Edgar Allan Poe, por quem Lovecraft nutria profunda afeição, e Lord Dunsany, cujas narrativas de fantasia inspiraram suas histórias em Terras de Sonho. Lovecraft é um dos poucos autores cuja obra literária não tem meio-termo: volta-se única e exclusivamente para o horror, tendo como finalidade perturbar o leitor, depois de atraí-lo para o ambiente e o clima daquilo que lê. Muitas vezes, H. P. Lovecraft parte de uma situação, à primeira vista, banal para, paulatinamente, revelar o horror por trás dela — assim acontece no romance O Caso de Charles Dexter Ward, no qual a atmosfera de normalidade se desfaz à medida que o autor revela, aos poucos, o resultado da pesquisa que o citado Charles fizera tentando encontrar seu antepassado que havia sido obscurecido propositadamente. 
</p>

<p>
	Além da atmosfera banal, outro ingrediente da fórmula lovecraftniana para seduzir o leitor é o uso da primeira pessoa: a maior parte de seus contos — como O Chamado de Cthulhu, Um Sussurro nas Trevas, A Cor que Caiu do Céu, Sombras Perdidas no Tempo e Nas Montanhas da Loucura — é narrada em primeira pessoa. Em algumas histórias, todos os acontecimentos são vividos pelo narrador, como em Sombras Perdidas no Tempo. Em outras, o narrador convive com alguns personagens e toma parte dos fatos (em geral, a pior delas). 
</p>

<p>
	As constantes referências nas histórias de Lovecraft a horrores, monstros e divindades ancestrais acabaram por gerar algo análogo a uma mitologia, popularmente conhecida como Cthulhu Mythos, expressão criada após a morte do autor pelo escritor August Derleth, um dos muitos escritores a basearem suas histórias na mitologia criada por Lovecraft. O Cthulhu Mythos contém vários panteões de seres extra-dimensionais que reinaram sobre a Terra há milhões de anos. Estes seres eram tão poderosos que eram ou podiam ser considerados deuses. Alguns deles, inclusive, teriam sido os responsáveis pela criação da raça humana e teriam uma intervenção direta em toda a história do universo. Algumas das histórias de Lovecraft mostram a paixão deste por gatos, que o levou a criar a fictícia cidade de Ulthar. Esta última é mencionada na história "Em Sonhos, à Procura da Desconhecida Kadath".
</p>
